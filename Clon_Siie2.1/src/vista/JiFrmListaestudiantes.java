   private void cargarEstudiantes() {
        // Modelo no editable
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // ðŸ”’ ninguna celda editable
            }
        };

        // Definir columnas
        modelo.addColumn("CC");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Edad");
        modelo.addColumn("TelÃ©fono");
        modelo.addColumn("Correo");

        // Asignar modelo a la tabla
        tablaEstudiantes.setModel(modelo);

        // ðŸ”¹ Usamos el procedimiento almacenado
    String sql = "CALL listar_estudiantes_por_docente_materia(?, ?)"; 

try (Connection conn = baseDatos.getConnection();
     PreparedStatement ps = conn.prepareStatement(sql)) {

    ps.setString(1, profesor);
    ps.setString(2, materia);
    ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Object[] fila = {
                    rs.getString("cc"),
                    rs.getString("nombre"),
                    rs.getString("apellido"),
                    rs.getString("edad"),
                    rs.getString("telefono"),
                    rs.getString("correo")
                };
                modelo.addRow(fila);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                "Error cargando estudiantes: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


 

