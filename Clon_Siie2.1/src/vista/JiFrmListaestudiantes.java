    private void cargarEstudiantes() {
    // Modelo no editable
    DefaultTableModel modelo = new DefaultTableModel() {
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // ðŸ”’ ninguna celda editable
        }
    };

    // Definir columnas
    modelo.addColumn("CC");
    modelo.addColumn("Nombre");
    modelo.addColumn("Apellido");
    modelo.addColumn("Edad");
    modelo.addColumn("TelÃ©fono");
    modelo.addColumn("Correo");

    // Asignar modelo a la tabla
    tablaEstudiantes.setModel(modelo);

    String sql = "SELECT a.cc, a.nombre, a.apellido, a.edad, a.telefono, a.correo\n" +
"        FROM Materias m\n" +
"        INNER JOIN Alumnos a ON m.alumno_id = a.id\n" +
"        INNER JOIN Docentes d ON m.docente_id = d.id\n" +
"        WHERE d.nombre = ? AND m.estado = 'activa';";

    try (Connection conn = baseDatos.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {

        ps.setString(1, profesor); // ðŸ‘ˆ profesor que viene del constructor
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            Object[] fila = {
                rs.getString("cc"),
                rs.getString("nombre"),
                rs.getString("apellido"),
                rs.getString("edad"),
                rs.getString("telefono"),
                rs.getString("correo"),
            };
            modelo.addRow(fila);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this,
            "Error cargando estudiantes: " + e.getMessage(),
            "Error", JOptionPane.ERROR_MESSAGE);
    }
}



 

